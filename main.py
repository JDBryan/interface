
def subtract(num_a, num_b):
    return num_a - num_b

def times(num_a, num_b, num_c):
    return num_a * num_b * num_c

def addition(num_a, num_b, num_c):
    return num_a + num_b + num_c

def divide(num_a, num_b):
    return num_a / num_b

print(subtract(3, 4))
print(subtract(100, 43))

print(times(3, 4, 10))
print(times(100, 43, 2))

print(addition(1, 2, 3))

print(divide(100, 10))
print(divide(16, 4))